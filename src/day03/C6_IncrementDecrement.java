package day03;

public class C6_IncrementDecrement {

	public static void main(String[] args) {
		
		int number=10;
		
		// bir variable'in degerini toplayarak arttirmak icin 3 yontem kullanabilirim
		
		// 1.yontem (acemi yontemi)
		
		number = number + 5;
		
		// 2.yontem (usta isi)
		
		number += 8;
		
		// 3.yontem sadece 1 (1) arttirmak icin
		
		number++;
		
		// bu yontemler sadece toplama islemine has degildir tum operatorlar icin kullanilir
		// 3.yontem sadece toplama ve cikarma icin gecerlidir
		
		// number'i 6 eksiltin
		
		number -= 6;
		// number'i 2'ye bolun
		
		number /= 2;
		
		// number'i 3 ile carpin
		
		number *= 3;
		
		// number'i 1 eksiltin
		
		number--;
		
		
				
		
		

	}

}
